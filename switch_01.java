
public class switch_01 {

	public static void main(String[] args) {
		int value=2;
		
		switch(value)
		{
			case 1:System.out.println("값은 1입니다.");
				break;
			case 2:System.out.println("값은 2입니다.");
				break;
			case 3:System.out.println("값은 3입니다.");
				break;
			case 4:System.out.println("값은 4입니다.");
				break;
		}
	}

}
